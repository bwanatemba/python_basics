text = "Hello World"
course = "Web"

print(text)

# printing/passing index positions (Accessing an element in a string)
print(text[0])  # "H" takes index position 0
print(text[6])  # "W" takes index position 6

# Determine the size of a string
print(len(text))

# Modifying a String
print(text.upper())
print(course.lower())

# String Concatenation- combining/joining strings
first = "Hello"
last = "There"

print(first+last)
print(first+" "+last)
