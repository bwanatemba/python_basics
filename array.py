
courses = ["MIT", "CyberSecurity", "DataScience"]

print(courses)


# Assessing an element in an Array
print(courses[1])

# Looping through an Array
for course in courses:
    print(course)

# Adding an additional element in an Array
courses.append("Android Development")
print(courses)

# Deleting an element from an Array
courses.remove("MIT")
print(courses)